var Linear = function() {};
Linear.easeNone = function(e, t, n, r) {
        return n * e / r + t
    },
    Linear.easeIn = function(e, t, n, r) {
        return n * e / r + t
    },
    Linear.easeOut = function(e, t, n, r) {
        return n * e / r + t
    },
    Linear.easeInOut = function(e, t, n, r) {
        return n * e / r + t
    };
